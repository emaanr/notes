/**
 * Variables
 *      Class that demonstrates different variable data types.
 */
public class Variables {

    public static void main(String[] args) {

        int i = 25;
        double d = 3.14;
        float f = 3.14f;
        boolean b = true;
        char c = 'C';
        String s = "Emaan";

        System.out.println("Integer i is " + i);
        System.out.println("Double d is " + d);
        System.out.println("Float f is " + f);
        System.out.println("Boolean b is " + b);
        System.out.println("Char c is " + c);
        System.out.println("String s is " + s);
    }
}
